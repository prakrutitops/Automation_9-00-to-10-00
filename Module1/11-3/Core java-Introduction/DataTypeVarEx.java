
public class DataTypeVarEx 
{
	
	public static void main(String[] args) 
	{
		//datatype variable = value;
		int num=6;
		int num2=5;
		
		int ans = num + num2;
		int ans2 = num - num2;
		int ans3 = num * num2;
		int ans4 = num / num2;
		
		
		System.out.println(ans);
		System.out.println(ans2);
		System.out.println(ans3);
		System.out.println(ans4);
		
		
	}

	
}
